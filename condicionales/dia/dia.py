#programa que nos dice que dia de la semana es
op = int(input("Introduzca dia de la semana: "))

if op ==1:
	print("Lunes")
elif op ==2:
	print("Martes")
elif op ==3:
	print("Miercoles")
elif op ==4:
	print("Jueves")
elif op ==5:
	print("Viernes")
elif op ==6:
	print("Sabado")
elif op ==7:
	print("Domingo")
else:
	print("ERROR: Dia incorrecto.")
